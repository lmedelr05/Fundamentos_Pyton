my_string = "esTe eS Mi StrInG"

# longitud de string
print(len(my_string))
# Mayusculas
print(my_string.upper())
# Minusculas
print(my_string.lower())
# Capitalice primera letra a mayus
print(my_string.capitalize())
# Eliminar espacio inicio fin
print(my_string.strip())

# Indexing and Slicing
my_string = "hola 3C DSM"
print(my_string[0])
print(my_string[6])
print(my_string[3])
print(my_string[-1])
print(my_string[-2])

print(my_string[0:2])
print(my_string[5:7])
print(my_string[5:-1])



